    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // 1. Obtener la fila seleccionada
        int filaSeleccionada = tblFuncionarios.getSelectedRow();
        
        // 2. Validar si se seleccionó una fila
        if (filaSeleccionada == -1) {
            // Si es -1, no hay nada seleccionado
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar un funcionario de la tabla para eliminar.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return; // Detenemos la ejecución
        }
        
        // 3. Obtener los IDs (Clave Primaria) de la fila seleccionada
        // (Columna 0 = Tipo ID, Columna 1 = Número ID)
        // Obtenemos el valor de la celda y lo convertimos a String
        String tipoId = String.valueOf(tblFuncionarios.getValueAt(filaSeleccionada, 0));
        String numeroId = String.valueOf(tblFuncionarios.getValueAt(filaSeleccionada, 1));
        
        // 4. Pedir confirmación (¡MUY IMPORTANTE!)
        int confirmacion = javax.swing.JOptionPane.showConfirmDialog(this, 
                "¿Está seguro de que desea eliminar al funcionario: " + numeroId + "?", 
                "Confirmar Eliminación", 
                javax.swing.JOptionPane.YES_NO_OPTION);
        
        // 5. Si el usuario presiona "Sí" (YES_OPTION es 0)
        if (confirmacion == javax.swing.JOptionPane.YES_OPTION) {
            // 6. Llamar al DAO para eliminar
            IFuncionarioDAO funcionarioDAO = new FuncionarioDAOImpl();
            if (funcionarioDAO.eliminar(tipoId, numeroId)) {
                // Si el DAO devuelve true (éxito)
                javax.swing.JOptionPane.showMessageDialog(this, "Funcionario eliminado exitosamente.", "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            } else {
                // Si el DAO devuelve false (error)
                javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar el funcionario.", "Error de BD", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
            
            // 7. Refrescar la tabla (pase lo que pase, para mostrar el estado actual)
            cargarDatosEnTabla();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
      // 1. Obtener la fila seleccionada
        int filaSeleccionada = tblFuncionarios.getSelectedRow();
        
        // 2. Validar si se seleccionó una fila
        if (filaSeleccionada == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar un funcionario de la tabla para editar.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // 3. Obtener los IDs (Clave Primaria) de la fila seleccionada
        String tipoId = String.valueOf(tblFuncionarios.getValueAt(filaSeleccionada, 0));
        String numeroId = String.valueOf(tblFuncionarios.getValueAt(filaSeleccionada, 1));
        
        // 4. Obtener el objeto Funcionario COMPLETO usando el DAO
        IFuncionarioDAO funcionarioDAO = new FuncionarioDAOImpl();
        Funcionario funcionarioParaEditar = funcionarioDAO.obtenerPorId(tipoId, numeroId);
        
        // 5. Validar que el funcionario exista (por si acaso)
        if (funcionarioParaEditar == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "No se pudo encontrar el funcionario para editar.", "Error de Carga", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // 6. Abrir el formulario usando el NUEVO constructor (el de editar)
        //    Le pasamos el funcionario que encontramos.
        FormularioFuncionario formulario = new FormularioFuncionario(this, true, funcionarioParaEditar);
        formulario.setVisible(true); // El código se pausa aquí
        
        // 7. Refrescar la tabla después de que el formulario se cierre
        cargarDatosEnTabla();
    }//GEN-LAST:event_btnEditarActionPerformed

