    /**
     * Método privado para cargar o recargar los datos de la base de datos en la JTable.
     * Es reutilizable y mantiene el constructor limpio.
     */
    private void cargarDatosEnTabla() {
        // 1. Instanciamos nuestro DAO
        // Usamos la Interfaz como tipo, pero creamos la Implementación.
        IFuncionarioDAO funcionarioDAO = new FuncionarioDAOImpl();
        
        // 2. Definimos los títulos de las columnas para nuestra tabla
        String[] titulos = {"Tipo ID", "Número ID", "Nombres", "Apellidos", "Estado Civil", "Sexo", "Teléfono"};
        
        // 3. Creamos un "modelo" para la tabla. No podemos añadir datos a la JTable directamente.
        // Hacemos que la tabla no sea editable por el usuario.
        DefaultTableModel tableModel = new DefaultTableModel(titulos, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Todas las celdas no son editables
            }
        };

        // 4. Obtenemos la lista de funcionarios desde la base de datos
        // ¡Aquí es donde usamos nuestro DAO!
        List<Funcionario> listaFuncionarios = funcionarioDAO.obtenerTodos();
        
        // 5. Recorremos la lista y la añadimos al modelo de la tabla
        for (Funcionario f : listaFuncionarios) {
            Object[] fila = new Object[7]; // Un objeto por cada columna
            fila[0] = f.getTipoIdentificacion();
            fila[1] = f.getNumeroIdentificacion();
            fila[2] = f.getNombres();
            fila[3] = f.getApellidos();
            fila[4] = f.getEstadoCivil();
            fila[5] = f.getSexo();
            fila[6] = f.getTelefono();
            tableModel.addRow(fila); // Añadimos la fila al modelo
        }
        
        // 6. Finalmente, le decimos a nuestra tabla (tblFuncionarios) que use este modelo
        tblFuncionarios.setModel(tableModel);
    }
    // --- FIN DEL NUEVO MÉTODO ---
    
    

