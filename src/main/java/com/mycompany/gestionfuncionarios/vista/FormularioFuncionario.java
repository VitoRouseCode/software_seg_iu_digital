    /**
     * Rellena los campos del formulario con los datos del 'funcionarioAEditar'.
     */
    private void cargarDatosParaEdicion() {
        if (this.funcionarioAEditar != null) {
            // Rellenamos todos los campos
            txtTipoId.setText(funcionarioAEditar.getTipoIdentificacion());
            txtNumeroId.setText(funcionarioAEditar.getNumeroIdentificacion());
            txtNombres.setText(funcionarioAEditar.getNombres());
            txtApellidos.setText(funcionarioAEditar.getApellidos());
            txtEstadoCivil.setText(funcionarioAEditar.getEstadoCivil());
            txtSexo.setText(funcionarioAEditar.getSexo());
            txtDireccion.setText(funcionarioAEditar.getDireccion());
            txtTelefono.setText(funcionarioAEditar.getTelefono());
            
            // Formateamos la fecha a String
            if (funcionarioAEditar.getFechaNacimiento() != null) {
                txtFechaNacimiento.setText(formatoFecha.format(funcionarioAEditar.getFechaNacimiento()));
            }
            
            // --- IMPORTANTE: Hacemos que la clave primaria (IDs) NO se pueda editar ---
            txtTipoId.setEditable(false);
            txtNumeroId.setEditable(false);
        }
    }
   

