    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
      try {
            // 1. RECOLECCIÓN DE DATOS
            String tipoId = txtTipoId.getText().trim();
            String numeroId = txtNumeroId.getText().trim();
            String nombres = txtNombres.getText().trim();
            String apellidos = txtApellidos.getText().trim();
            String sexo = txtSexo.getText().trim();

            // 2. VALIDACIÓN (Campos obligatorios)
            if (tipoId.isEmpty() || numeroId.isEmpty() || nombres.isEmpty() || apellidos.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Los campos ID, Nombres y Apellidos son obligatorios.", "Error de Validación", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Validamos la longitud del campo sexo
            if (sexo.length() > 1) {
                javax.swing.JOptionPane.showMessageDialog(this, "El campo 'Sexo (M / F)' solo acepta un carácter.", "Error de Validación", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // 3. RECOLECCIÓN DEL RESTO DE DATOS
            String estadoCivil = txtEstadoCivil.getText();
            String direccion = txtDireccion.getText();
            String telefono = txtTelefono.getText();
            
            // 4. MANEJO DE EXCEPCIÓN ESPECIAL (FECHA)
            java.util.Date fechaNac;
            try {
                fechaNac = formatoFecha.parse(txtFechaNacimiento.getText());
            } catch (java.text.ParseException e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto. Use aaaa-mm-dd.", "Error de Fecha", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }

            // 5. CREACIÓN DEL OBJETO (Modelo)
            Funcionario funcionario = new Funcionario();
            funcionario.setTipoIdentificacion(tipoId);
            funcionario.setNumeroIdentificacion(numeroId);
            funcionario.setNombres(nombres);
            funcionario.setApellidos(apellidos);
            funcionario.setEstadoCivil(estadoCivil);
            funcionario.setSexo(sexo);
            funcionario.setDireccion(direccion);
            funcionario.setTelefono(telefono);
            funcionario.setFechaNacimiento(fechaNac);

            // 6. LLAMADA AL DAO (AQUÍ ESTÁ LA LÓGICA DE EDICIÓN)
            IFuncionarioDAO funcionarioDAO = new FuncionarioDAOImpl();
            boolean exito;
            
            if (this.funcionarioAEditar == null) {
                // --- MODO CREAR ---
                exito = funcionarioDAO.crear(funcionario);
            } else {
                // --- MODO ACTUALIZAR ---
                exito = funcionarioDAO.actualizar(funcionario);
            }
            
            // 7. RESPUESTA
            if (exito) {
                String mensaje = (this.funcionarioAEditar == null) ? "Funcionario guardado exitosamente." : "Funcionario actualizado exitosamente.";
                javax.swing.JOptionPane.showMessageDialog(this, mensaje, "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                this.dispose(); // Cierra la ventana
            } else {
                String mensaje = (this.funcionarioAEditar == null) ? "Error al guardar el funcionario." : "Error al actualizar el funcionario.";
                javax.swing.JOptionPane.showMessageDialog(this, mensaje, "Error de BD", javax.swing.JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ocurrió un error inesperado: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

